<p>
<a href="#" class="faq_question" >How does the partnership between Kaffa Karma and my Non-Profit group or organization work?</a>
<div class="faq_answer" >1. Kaffa Karma will create a personalized web site for your Non-Profit Organization FREE of charge to you.<br/>
2. You promote coffee sales through the Non-Profit website to generate income for your group. Buy your own coffee and refer others to do the  same.<br/> 
3. Kaffa Karma does all the time consuming work for you by handling all your Non-Profit group sales and product delivery. You just handle the promotion.</div>
</p>


<p>
<a href="#" class="faq_question" >How does Kaffa Karma help my Non-Profit group make money?</a>
<div class="faq_answer" >At the end of the month, all sales for the Non-Profit group are totaled and a check is mailed for a percentage of the sales. This is truly stress-free fund raising!</div>
</p>



